package practicaltask_1;

public class Type_conversion {

	public static void main(String[] args) {

		        int a = 100;
		        double b = a;
		        System.out.println("Implicit conversion: int to double -> " + b);

		        double c = 9.78;
		        int d = (int) c;
		        System.out.println("Explicit conversion: double to int -> " + d);
		    }

	}


